package com.safegergis.tome_user_data.enums;

/**
 * Types of activities that can appear in the activity feed
 */
public enum ActivityType {
    /**
     * User logged a reading session
     */
    READING_SESSION,

    /**
     * User created a new list
     */
    LIST_CREATED,

    /**
     * User finished reading a book
     */
    BOOK_FINISHED
}
